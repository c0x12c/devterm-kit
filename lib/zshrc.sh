# ── Main entry point ──────────────────────────────────────────────────────

generate_zshrc() {
  log_step "Configuring shell config"

  _detect_zshrc_scenario

  case "$ZSHRC_SCENARIO" in
    A) log_info "Fresh install — creating split config" ;;
    B) log_info "Migrating old monolithic .zshrc to split format" ;;
    C) log_info "Split config detected — regenerating ~/.zshrc.devterm" ;;
    D) log_info "Existing .zshrc detected — preserving in ~/.zshrc.local" ;;
  esac

  _handle_migration

  # Backup .zshrc.devterm before regenerating (if it exists)
  backup_file "$ZSHRC_DEVTERM"

  _generate_zshrc_devterm

  log_ok "$HOME/.zshrc.devterm generated"
  log_info "Custom config: edit ~/.zshrc.local — run 'devterm tips' for cheat sheet"
}
