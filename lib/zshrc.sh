#!/usr/bin/env bash
# lib/zshrc.sh — Generate split .zshrc config (loader + devterm + local)
#
# File structure:
#   ~/.zshrc          Thin loader (sources .zshrc.devterm + .zshrc.local)
#   ~/.zshrc.devterm  Managed by devterm — regenerated every setup.sh run
#   ~/.zshrc.local    User's custom config — NEVER touched by devterm

ZSHRC="$HOME/.zshrc"
ZSHRC_DEVTERM="$HOME/.zshrc.devterm"
ZSHRC_LOCAL="$HOME/.zshrc.local"

# ── Migration scenario detection ─────────────────────────────────────────

# Sets ZSHRC_SCENARIO to A, B, C, or D
_detect_zshrc_scenario() {
  if [[ ! -f "$ZSHRC" ]]; then
    ZSHRC_SCENARIO="A"  # Fresh install — no .zshrc at all
  elif grep -q "Managed by devterm" "$ZSHRC" 2>/dev/null; then
    ZSHRC_SCENARIO="C"  # Already migrated to split format
  elif grep -q "Generated by devterm" "$ZSHRC" 2>/dev/null; then
    ZSHRC_SCENARIO="B"  # Old monolithic devterm .zshrc
  else
    ZSHRC_SCENARIO="D"  # Non-devterm .zshrc (user's own config)
  fi
}

# ── Migration handler ─────────────────────────────────────────────────────

_handle_migration() {
  case "$ZSHRC_SCENARIO" in
    A)
      # Fresh install — create all 3 files
      _write_loader
      _write_zshrc_local_template
      ;;
    B)
      # Old monolithic devterm .zshrc — backup and migrate
      backup_file "$ZSHRC"
      _write_loader
      _write_zshrc_local_template
      log_info "Migrated to split config. Move custom additions to ~/.zshrc.local"
      ;;
    C)
      # Already migrated — only regenerate .zshrc.devterm
      # Touch nothing else
      ;;
    D)
      # Non-devterm .zshrc — preserve user config
      if [[ ! -f "$ZSHRC_LOCAL" ]]; then
        # Move existing .zshrc to .zshrc.local (preserve user config!)
        mv "$ZSHRC" "$ZSHRC_LOCAL"
        log_info "Moved existing ~/.zshrc → ~/.zshrc.local (your config is preserved)"
      else
        # .zshrc.local already exists — don't clobber it
        backup_file "$ZSHRC"
        log_info "Existing ~/.zshrc backed up (your ~/.zshrc.local was kept)"
      fi
      _write_loader
      ;;
  esac
}

# ── Loader file (~/.zshrc) ────────────────────────────────────────────────

_write_loader() {
  cat > "$ZSHRC" << 'LOADER_EOF'
# =============================================================================
# ~/.zshrc — Managed by devterm (https://github.com/c0x12c/devterm-kit)
# DO NOT edit this file — it is regenerated during migration.
#
# Your config goes in:  ~/.zshrc.local   (never overwritten by devterm)
# devterm-managed:      ~/.zshrc.devterm (regenerated every setup.sh run)
# =============================================================================

# Load devterm-managed config
[[ -f "$HOME/.zshrc.devterm" ]] && source "$HOME/.zshrc.devterm"

# Load your personal customizations (safe — never overwritten)
[[ -f "$HOME/.zshrc.local" ]] && source "$HOME/.zshrc.local"
LOADER_EOF
}

# ── User local template (~/.zshrc.local) ──────────────────────────────────

_write_zshrc_local_template() {
  # Only create if it doesn't exist — NEVER overwrite
  [[ -f "$ZSHRC_LOCAL" ]] && return 0

  cat > "$ZSHRC_LOCAL" << 'LOCAL_EOF'
# =============================================================================
# ~/.zshrc.local — Your personal shell customizations
# This file is NEVER overwritten by devterm. Put your custom config here.
#
# Examples:
#   export MY_API_KEY="xxx"
#   alias myproject="cd ~/Work/my-project"
#   source "$HOME/.cargo/env"
# =============================================================================
LOCAL_EOF
}

# ── Devterm managed config (~/.zshrc.devterm) ─────────────────────────────

